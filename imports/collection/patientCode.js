export const Co_PatientCode = new Meteor.Collection("co_patientCode");

Co_PatientCode.schema = new SimpleSchema({
    code: {
        type: String
    },
    type: {
        type: String
    },
    patientId: {
        type: String,
    },
    lastDate: {
        type: String
    },
    rolesArea: {
        type: String,
        optional: true
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }

})

